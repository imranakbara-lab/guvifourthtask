package quvitaskfor;

public class ifandwhileloop {

	public static void main (String[] args) {
		
		System.out.println("USING FOR LOOP:");
		for (int i=10; i<=50; i++) {
		System.out.println(i);
		}
		
		System.out.println("USING WHILE LOOP");
		int j=10;
		while(j<=50) {
			System.out.println(j);
			j++;
		}
		
		
	}
	
}
